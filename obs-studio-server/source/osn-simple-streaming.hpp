	struct signalInfo {
		std::string signal;
		int code;
		std::string errorMessage;
	};

    class SimpleStreaming
    {
        public:
        SimpleStreaming() {
			videoEncoder = nullptr;
			service = nullptr;
			output = nullptr;
			audioBitrate = 160;
			enforceServiceBitrate = true;
			enableTwitchVOD = false;
			signals = {
				"start",
				"stop",
				"starting",
				"stopping",
				"activate",
				"deactivate",
				"reconnect",
				"reconnect_success"
			};
			delay = new Delay();
		}
        ~SimpleStreaming() {}

        public:
        obs_encoder_t* videoEncoder;
        obs_encoder_t* audioEncoder;
		obs_service_t* service;
		obs_output_t* output;
        bool enforceServiceBitrate;
        bool enableTwitchVOD;
        uint32_t audioBitrate;
		std::vector<std::string> signals;
		Delay* delay;

		std::mutex signalsMtx;
		std::queue<signalInfo> signalsReceived;
    };

	struct cbData {
		std::string signal;
		SimpleStreaming* stream;
	};

